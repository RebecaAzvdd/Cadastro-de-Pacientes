private DefaultTableModel montarTabela(){      
       DefaultTableModel tabela1 = new DefaultTableModel();
       tabela1.addColumn("ID");
       tabela1.addColumn("Nome");
       tabela1.addColumn("Telefone");
       tabela1.addColumn("CPF");
       tabela1.addColumn("Data");
       tabela1.addColumn("Paciente");
       tabela1.addColumn("Consulta");

       for(int i = 0; i < lista.size(); i++){
          Pessoa pe = lista.get(i);
          String pronto = "";
          if(pe.isRealizada()){
           pronto = "Sim";
       }else{
           pronto = "NÃ£o";
       }
          String[] linha = {
           Integer.toString(pe.getId()),
           pe.getNome(),
           pe.getTelefone(),
           pe.getCpf(),
           pe.getData(),
           pe.getCheckbox(),
           pronto
               }; 
          tabela1.addRow(linha);
      }
      return tabela1;
   }
   private int getPosicao(){
       int posicao = tabela.getSelectedRow();
       return posicao;
   }
    
    
    
    

